    public void addTable(int i, String id) // code for adding item to the display table
    {
        DefaultTableModel dt = (DefaultTableModel) jTable1.getModel();
        Vector v = new Vector();
        v.add(id);
        v.add(checkout.getItemName(i));
        v.add("₱" + checkout.getItemPrice(i));
        dt.addRow(v);
    }

